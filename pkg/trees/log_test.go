// @ trusted
func TestTreeFromProof(t *testing.T) {
	for i := range len(testLeafs) {
		size := uint64(i + 1)
		t.Run(
			fmt.Sprintf("size %d", size),
			func(t *testing.T) {
				serverTree := FullTree(testLeafs[:size])
				prf := logTreePrunedProof(t, serverTree, 0)
				var clientTree *Log
				var err error
				if clientTree, err = clientTree.Grow(size, prf); err != nil {
					t.Fatalf("could not grow tree: %v", err)
				}
				assertSize(t, clientTree, size)
				assertContainsFrontier(t, clientTree)
				assertEqualRootHashes(t, serverTree, clientTree)
			},
		)
	}
}

// @ trusted
func TestProofSize(t *testing.T) {
	tests := []struct {
		from      uint64
		to        uint64
		proofSize int
	}{
		{from: 1, to: 8, proofSize: 6},
	}

	for _, tc := range tests {
		t.Run(fmt.Sprintf("proof size from %d to %d", tc.from, tc.to), func(t *testing.T) {
			tree := FullTree(testLeafs[:tc.to])
			prf := logTreePrunedProof(t, tree, tc.from)
			if prf == nil {
				t.Fatalf("prf is nil")
			} else if prf.Elements == nil {
				t.Fatalf("prf.Elements is nil")
			} else if len(prf.Elements) != tc.proofSize {
				t.Fatalf("len(prf.Elements) = %d; expected: %d", len(prf.Elements), tc.proofSize)
			}
		})
	}
}

// @ trusted
func TestGrowTreeFromProof(t *testing.T) {
	for oldIndex := range len(testLeafs) {
		oldSize := uint64(oldIndex + 1)
		for newIndex := oldSize; newIndex < uint64(len(testLeafs)); newIndex++ {
			newSize := newIndex + 1
			t.Run(
				fmt.Sprintf("from %d to %d", oldSize, newSize),
				func(t *testing.T) {
					serverTree := FullTree(testLeafs[:oldSize])
					prf := logTreePrunedProof(t, serverTree, 0)
					var clientTree *Log
					var err error
					if clientTree, err = clientTree.Grow(oldSize, prf); err != nil {
						t.Fatalf("could not grow tree: %v", err)
					}

					serverTree = FullTree(testLeafs[:newSize])
					prf = logTreePrunedProof(t, serverTree, clientTree.size)
					if clientTree, err = clientTree.Grow(newSize, prf); err != nil {
						t.Fatalf("could not grow tree: %v", err)
					}

					assertSize(t, clientTree, newSize)
					assertContainsFrontier(t, clientTree)
					assertEqualRootHashes(t, serverTree, clientTree)
				},
			)
		}
	}
}

// @ trusted
func logTreePrunedProof(t *testing.T, tree *Log, cacheSize uint64) *proofs.InclusionProof {
	t.Helper()

	tree.Prune(cacheSize)
	prf, err := tree.ProofFromPruned(cacheSize)
	if err != nil {
		t.Fatalf("ProofFromPruned(cacheSize=%d): %v", cacheSize, err)
	} else if prf == nil {
		t.Fatal("ProofFromPruned returned nil")
	}
	return prf
}

// @ trusted
func assertSize(t *testing.T, tree *Log, expected uint64) {
	t.Helper()

	if tree == nil {
		return
	} else {
		t.Logf("Checking for size %d", expected)
		if tree.size != expected {
			t.Fatalf("tree.size = %d, expected %d", tree.size, expected)
		} else if tree.left != nil {
			if tree.right == nil {
				t.Fatalf("tree.right == nil, but t.left != nil")
			}
			// First assert checks size of all right trees in left subtree
			t.Log("Recurse left")
			assertSize(t, tree.left, tree.left.size)
			t.Log("Recurse right")
			assertSize(t, tree.right, tree.size-tree.left.size)
			t.Log("Return")
		} else {
			t.Log("Return")
		}
	}
}

// @ trusted
func assertContains(t *testing.T, tree *Log, want []*[sha256.Size]byte) {
	t.Helper()

	for i, wantHash := range want {
		if got, err := tree.GetLeafHash(uint64(i)); err != nil {
			t.Fatalf("GetLeafHash(%d): %v", i, err)
		} else if got == nil {
			t.Fatalf("GetLeafHash(%d) = nil", i)
		} else if *got != *wantHash {
			t.Fatalf("GetLeafHash(%d) = %x, want %x", i, *got, *wantHash)
		}
	}
}

// @ trusted
func assertContainsFrontier(t *testing.T, tree *Log) {
	t.Helper()

	frontier := search.Frontier(tree.size)
	for _, i := range frontier {
		if h, err := tree.GetLeafHash(i); err != nil {
			t.Fatalf("GetLeafHash(%d) raised: %v", i, err)
		} else if h == nil {
			t.Fatalf("GetLeafHash(%d) is nil", i)
		} else if *h != *testLeafs[i] {
			t.Fatalf("GetLeafHash(%d) = %x, want %x", i, *h, *testLeafs[i])
		}
	}
}

// @ trusted
func assertEqualRootHashes(t *testing.T, left *Log, right *Log) {
	lr := left.GetRoot()
	rr := right.GetRoot()
	if !bytes.Equal((*lr)[:], (*rr)[:]) {
		t.Fatalf("Root hashes should be equal but: %v != %v", *lr, *rr)
	}
}
